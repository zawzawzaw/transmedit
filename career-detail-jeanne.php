              <div id="page-article-text-container">
                <p>In March 2016 we went to Stamford, Connecticut, USA for software and application training on the Immucor transplant products.</p>
                <p>Immucor is a US based company which specializes in transfusion- and transplant-related diagnostic products, with branches in Europe and South America and a vast distributor network in Asia.</p>
                <p>The focus of our training was the LIFECODES group of products. We spent a lot of time in the lab, running the actual assays and learning the analyser software. The other part of our training included performing results analysis using the LIFECODES proprietary software.</p>
                <p>Our training was conducted by members of different departments within Immucor, which gave us in-depth view of the products from various angles. The Tech Support team guided us through the software and hands-on lab sessions. The Logistics department educated us on the complexities of the international shipping process. The Business Development team gave us an overview of the transplant diagnostics market, our competitive advantages and our growth strategies.</p>
                <p>We are very grateful to Immucor for their support and hospitality. It was an awesome learning experience and we are ready to serve our customers better at home.</p>
                <p>Ultimately, Transmedic is continuously looking for ways to expand the knowledge and skills of its employees so we can provide not only the best medical products to the customers, but also the best possible support.</p>

              <div id="page-article-text-container">
                <p>My primary role as a service engineer in Transmedic is to perform repairs and scheduled maintenance on the Tomotherapy Radiation Therapy machine.</p>
                <p>The Tomo machine requires periodic maintenance throughout the year and a lot of man hours are required for its upkeep. Besides these planned sessions, I also receive ad hoc service calls which usually happen during office hours. At times, my role also gives me the opportunity to visit regional sites to perform servicing.</p>
                <p>I usually begin my day with two eggs, a cup of tea, and a quick prayer that my phone doesn’t ring during breakfast. Thankfully most of the time it doesn’t, making this my favourite part of the day.</p>
                <p>My working hours aren’t fixed but as a general rule of thumb, my day starts when most people have finished theirs. During weekends my schedule varies, depending on whether treatments are being run on site. Anyhow, I’m always on standby 24 hours a day, 7 days a week.</p>
                <p>The biggest challenge for me comes when I have to perform urgent repairs on the Tomo machine. This requires me to perform well under pressure, as I have to properly manage customers’ expectations and make a fast and accurate diagnosis of the issue at the same time. Over time, I have honed my problem-solving skills and am now able to think quickly on my feet. On a side note, I have also become braver as another challenge that I had to overcome at the start was my fear of working alone in the hospital at night!</p>
                <p>I have been with Transmedic for almost a year now and I like what I’m doing. I come from an aviation background; back then I was handling systems associated with electronics. Having worked on the Tomo machine for 6 months, I find it more interesting to work on a relatively complex machine as it gives you the opportunity to gain experience in mechanical, electrical and computing systems, on top of electronics.</p>
                <p>As for the firm, the management is flexible and encourages you to try out new concepts and adopt new ideas. It is an environment where you can learn and grow with the company, and there is no hindrance to unleashing your creativity.</p>
